    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here: //Editar 
        if(jTable1.getSelectedRow() == -1){
            JOptionPane.showMessageDialog(null, "Escolha um item da tabela");
        }else{
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            int linha = jTable1.getSelectedRow();

            String descricaoAtual = String.valueOf(dtm.getValueAt(linha, 0));
            int prioridadeAtual = (int) (dtm.getValueAt(linha, 1));
            LocalDate prazoAtual = (LocalDate) dtm.getValueAt(linha, 3);

            EdicaoComPrazoView tfc = new EdicaoComPrazoView(descricaoAtual, prioridadeAtual, false, prazoAtual);
            this.dispose();
            tfc.setVisible(true);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

